    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        AccessTrainJPA jpa = new AccessTrainJPA();
        List<TrainStation> stations = jpa.getAllTrainStation();
        
        request.setAttribute("stations", stations);
        RequestDispatcher dispatcher = getServletContext().getRequestDispatcher("/newLine.jsp");
        dispatcher.forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String[] stationsID = request.getParameterValues("stations");
        
        int id = new Random().nextInt(1000000);
        
        AccessLineJPA lineJpa = new AccessLineJPA();       
        AccessTrainJPA trainJpa = new AccessTrainJPA();
        lineJpa.createLine(id);
        
        
        for(int i=0; i< stationsID.length ; i++){
            int res = trainJpa.updateLineForStation(Integer.parseInt(stationsID[i]), id);
        } 
        
        response.sendRedirect("lines.jsp");
    }

